import numpy as np

# Create array
arr = np.array([[1, 2, 3], [4, 5, 6]])

print("Array:\n", arr)

# a) Number of dimensions
print("Number of dimensions:", arr.ndim)

# b) Shape of array
print("Shape of array:", arr.shape)

# c) Size of array
print("Size of array:", arr.size)

# d) Type of elements
print("Data type of elements:", arr.dtype)